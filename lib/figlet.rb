require './escape.rb'
include EscSequence

class String
    def nonblank_length
        v_start = self =~ /(?<=\s)\S/
        v_end = self =~ /\s*$/
        v_end - v_start
    rescue StandardError
        0
    end

    def last_nonblank
        self =~ /\s*$/
    end
end

def figlet_char(v)
    return @column += 5 if v.match?(/ /)

    a = @font_big[v.to_sym].split(/\n/)

    max_len = a.map(&:last_nonblank).max
    @column += max_len

    cursor :line, 1
    a.map.with_index do |line, i|
        cursor :pos, @line + i, @column
        print line
    end
end

def figlet_test(v)
    @line = 1
    @column = 1

    screen :new
    cursor :save

    v.each_char { |c| figlet_char c }

    sleep 5
    screen :restore
end

